use tracing::{info, warn};
use vigil_core::config::LlmConfig;
use vigil_core::error::{VigilError, VigilResult};
use vigil_core::types::{NetworkEvent, TelemetryEnvelope};
use vigil_detect::results::AnomalyReport;

