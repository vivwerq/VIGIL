//! # VIGIL LLM Copilot (Phase 3)
//!
//! Air-gapped network anomaly diagnosis and mitigation assistant.
//! Translates structural `AnomalyReport` and `TelemetryEnvelope` structs into
//! human-readable diagnostic analysis, impact assessment, and step-by-step
//! remediation logs for NOC operators.
//!
//! ## Design Principles
//!
//! 1. **Offline Execution**: Fully local inference via `llama-cli` (llama.cpp) or `ollama` commands.
//! 2. **Fallback Expert System**: If no local LLM runtime or GGUF model file is available,
//!    falls back to a rule-based expert diagnostic generator to ensure continuous operation.
//! 3. **Zero-Trust Output Sanitization**: Since LLM outputs are untrusted, a sanitization pipeline
//!    inspects and redacts any suspicious command execution or script injection attempts before
//!    reports are sent to the operator or UI.

